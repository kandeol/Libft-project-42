char	*ft_strrchr(const char *s, int c)
{
	char	*tmp;

	tmp = 0;
	while (*s != '\0')
	{
		if (*s == (char)c)
			tmp = (char*)s;
		s++;
	}
	if (*s == (char)c)
		return ((char*)s);
	return (tmp);
}
